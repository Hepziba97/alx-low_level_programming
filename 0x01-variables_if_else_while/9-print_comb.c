#include <stdio.h>

/**
 * main - Entry point
 * Description - prints all posible combinations of single digit numbers
 * seperated by ,.
 * Return: 0 (Success)
 */
int main(void)
{
	int i;

	for (i = 48; i < 58; i++)
	{
		putchar(i);
		if (i != 57)
		{
			putchar(',');
			putchar(' ');
		}
	}
	putchar('\n');
	return (0);
}
